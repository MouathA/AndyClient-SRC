package optifine;

public class TextureAnimationFrame
{
    public int index;
    public int duration;
    public int counter;
    
    public TextureAnimationFrame(final int index, final int duration) {
        this.index = 0;
        this.duration = 0;
        this.counter = 0;
        this.index = index;
        this.duration = duration;
    }
}
