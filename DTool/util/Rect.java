package DTool.util;

public class Rect
{
    private float x;
    private float y;
    private float width;
    private float height;
    
    public Rect() {
    }
    
    public Rect(final float x, final float y, final float width, final float height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}
